package l4;

public class IfElseDemo {

    public static void main(String[] args) {

        boolean isRaining = false;

        System.out.println("I just woke up. Time to get ready for a new day.");

        if (isRaining) {
            System.out.println("Hello darkness my old friend..");
            System.out.println("I need an umbrella.. ella..");
        } else {
            System.out.println("Ahh.. Such a day.. Man..");
        }

        System.out.println("Let's go!");

    }

}
